function openmenu(){
	document.getElementsById("side-menu").style.display="block";
	document.getElementsById("menu-btn").style.display="none";
	document.getElementsById("close-btn").style.display="block";
}
function closemenu(){
	document.getElementsById("side-menu").style.display="none";
	document.getElementsById("menu-btn").style.display="block";
	document.getElementsById("close-btn").style.display="none";
}

